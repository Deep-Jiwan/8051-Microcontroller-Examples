;P1.0-P1.7 are connected to LCD data pins D0-D7
; P2.0 is connected to RS pin of LCD
; P2.1 is connected to R/W pin of LCD
; P2.2 is connected to E pin of LCD
ORG 0000H

MOV A,#38H
ACALL CMNWRT
ACALL DELAY

MOV A,#0EH
ACALL CMNWRT
ACALL DELAY

MOV A,#01H
ACALL CMNWRT
ACALL DELAY

MOV A,#06H
ACALL CMNWRT
ACALL DELAY

MOV A,#80H
ACALL CMNWRT
ACALL DELAY

MOV A,#'V'
ACALL DATWRT
ACALL DELAY

MOV A,#'I'
ACALL DATWRT
ACALL DELAY

MOV A,#'T'
ACALL DATWRT
ACALL DELAY
AGAIN: SJMP AGAIN

CMNWRT:
    MOV P1,A
    CLR P2.0
    CLR P2.1
    SETB P2.2
    CLR P2.2
RET

DATWRT:
    MOV P1,A
    SETB P2.0
    CLR P2.1
    SETB P2.2
    CLR P2.2
RET

DELAY: 
    MOV R3,#0FFH
    MOV R4,#0FFH
    MOV R5,#55H
    HERE: DJNZ R5,HERE
    HERE1: DJNZ R4, HERE1
    HERE2: DJNZ R3,HERE2
RET

END










